				<h3>2003-<?php echo date('Y'); ?></h3>
				<p>
					Depuis 2003, le Bagad de Nantes a repris les concours dans le cadre du championnat
					national des bagadoù. Pour ce championnat, les groupes sont classés en 5 catégories,
					ce qui représente 110 bagadoù, auxquels il faut ajouter tous les groupes non classés
					de Bretagne ou d’ailleurs. Il faut commencer par la 5 ème catégorie dont le concours
					est organisé dans le cadre du très connu Festival des Vieilles Charrues à Carhaix.
					D’année en année, à force d’implication et de travail,
					le Bagad de Nantes gravit les échelons et les catégories.
				</p>


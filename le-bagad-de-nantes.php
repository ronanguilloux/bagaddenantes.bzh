				<h2>Cinquante ans de musique bretonne à Nantes</h2>
				<p>
					Les bagadoù (pluriel de bagad signifiant « groupe » en breton) ont été créés
					en Bretagne à la fin des années 40 sur le principe des pipes bands écossais.
					Devant le succès rencontré, les bagadoù se multiplient rapidement à travers toute la Bretagne
					en passant bien sûr par Nantes avec la création dès 1950 de la Kevrenn de Nantes.
				</p>

				<img class="img-fluid shadow-sm p-2 mb-1 bg-body rounded" src="/assets/images/histoire/Gille-de-Retz.webp" alt="Le Bagad Gilles de Retz">

				<p>
					Les bagadoù créent un engouement fort auprès des jeunes et <a href="https://sonerion.bzh" target="_blank">Bodadeg Ar Sonerion</a>
					(l’assemblée des sonneurs) est créée pour fédérer et former ces jeunes recrues.
					On compte jusqu’à 4 bagadoù à Nantes à cette époque :
					<ul class="list-unstyled ps-3">
						<li>la Kevren de Nantes,</li>
						<li>le Bagad Alan Al Louarn,</li>
						<li>le Bagad Anne de Bretagne</li>
						<li>et le Bagad Gilles de Retz, devenu Bagad de Nantes, seul bagad Nantais encore existant de nos jours.</li>
					</ul>
				</p>
				<p>
					Aujourd’hui les bagadoù représentent la forme la plus connue et la plus dynamique
					de la tradition bretonne : leur rôle est donc essentiel pour faire perdurer,
					vivre et évoluer cette musique. Le Bagad de Nantes, ancré dans son époque,
					entend participer à cette évolution par une mise en valeur moderne et dynamique d’airs traditionnels.
				</p>


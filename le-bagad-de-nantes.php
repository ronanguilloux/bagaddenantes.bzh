				<p>
					Le Bagad de Nantes a été crée en 1968 sous le nom de Bagad Gilles de Retz.
					En 1989, pour affirmer son identité nantaise, il se transforma en
					Bagad de Nantes - Gilles de Retz. Enfin, c’est en 2000 qu’il a pris
					le nom de Bagad de Nantes, Bagad Naoned en breton.
				</p>

				<hr/>

				<h3>1989-2003</h3>
				<p>
					En 1989, après avoir longuement participé au championnat national des bagadoù (depuis 1975),
					le Bagad de Nantes décide d’arrêter les concours pour se consacrer à un autre projet.
					C’est ainsi que la formation traditionnelle s’est enrichi d’un 5 ème pupitre
					composé d’instruments modernes (guitare électrique, guitare basse, synthétiseur, batterie),
					afin d’offrir un répertoire alliant les traditions celtiques à un mélange de culture et de modernité.
					Le Bagad de Nantes a su en tirer partie en associant ses créations, le temps de concerts,
					à du rock celtique, à des rythmes brésiliens mais aussi à une expérience de
					musique classique avec l’ONPL (Orchestre National des Pays de la Loire),
					ainsi que l’orchestre philarmonique de Prague.
				</p>

